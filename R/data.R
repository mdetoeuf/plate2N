#'
#' \describe{
#'  \item{dataset,plate_id}{allowing the unique identification of the 96-well plate}
#'  \item{map}{identifier of samples, Standard curve or extractant (“extr_1" or "extr_2")}
#'  \item{extr_id}{extractant identifier corresponding to each well. 3 possible
#'      values: "extr_1", "extr_2" or "none" (for empty wells or standard curves)}
##' }
#'
